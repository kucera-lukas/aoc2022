import support

INPUT_TXT = os.path.join(os.path.dirname(__file__), 'input.txt')


def handle_cycle(
    screen: list[list[str]],
    cycle: int,
    val: int,
) -> list[list[str]]:
    i = cycle - 1

    if abs(val - (i % 40)) <= 1:
        screen[i // 40][i % 40] = '#'

    return screen


def compute(s: str) -> str:
    cycle, val = 0, 1
    screen = [['.' for _ in range(40)] for _ in range(6)]

    lines = s.splitlines()
    for line in lines:
        if line == 'noop':
            cycle += 1

            screen = handle_cycle(screen, cycle, val)
        else:
            to_add = support.parse_numbers_all(line, minus=True)[0]

            for _ in range(2):
                cycle += 1

                screen = handle_cycle(screen, cycle, val)

            val += to_add

    return '\n'.join(''.join(row) for row in screen)


